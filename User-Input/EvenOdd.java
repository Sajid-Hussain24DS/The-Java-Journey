class  EvenOdd {
    public static void main(String[] args){
        int number = 2;
        if(number / 2 == 0) {
            System.out.println("Given number is fully divisible by two then number is even");
        }else{
            System.out.println("Given number is fully divisible by two then number is odd");
            int number1 = 4;
            if(number1 % 4 == 0) {
                System.out.println("Given number is fully divisible by two and number is even");
            }else{
                System.out.println("Given number is not fully divisible by two and number is odd");
        }
    }
    }
}


