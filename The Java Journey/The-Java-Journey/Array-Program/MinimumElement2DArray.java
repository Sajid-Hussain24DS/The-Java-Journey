class MinimumElement2DArray{
    public static void main(String[] args) {
        int[] a = {90,30,45,83,1000};
        int mini = a[0];
    for(int i=1; i<a.length;i++){
        {
        if(a[i]<mini) {
            {
                mini=a[i];

        }
        System.out.println("Minimum Element of Array"+mini);

        }

        }

    }
}
}