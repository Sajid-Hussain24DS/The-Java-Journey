class LargestElement2DArray{
    public static void main(String[] args) {
        int[] a = {90,30,45,83,1000};
        int max = a[0];
    for(int i=1; i<a.length;i++){
        {
        if(a[i]>max) {
            {
                max=a[i];
                
        }
        System.out.println("Maximum Element of Array"+max);

        }

        }

    }
}
}